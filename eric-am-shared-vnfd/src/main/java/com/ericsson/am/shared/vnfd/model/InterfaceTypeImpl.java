package com.ericsson.am.shared.vnfd.model;

import java.util.Objects;

public class InterfaceTypeImpl extends InterfaceType {
    private String interfaceType;

    public String getInterfaceType() {
        return interfaceType;
    }

    public void setInterfaceType(String interfaceType) {
        this.interfaceType = interfaceType;
    }

    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        if (!super.equals(o)) {
            return false;
        }
        final InterfaceTypeImpl that = (InterfaceTypeImpl) o;
        return Objects.equals(interfaceType, that.interfaceType);
    }

    @Override
    public int hashCode() {
        return Objects.hash(super.hashCode(), interfaceType);
    }
}
