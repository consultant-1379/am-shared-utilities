package com.ericsson.am.shared.vnfd.model.servicemodel;

import java.util.Objects;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonProperty;

@JsonIgnoreProperties(ignoreUnknown = true)
@JsonInclude(JsonInclude.Include.NON_NULL)
public class Action {
    @JsonProperty("call_operation")
    private CallOperation callOperation;

    public CallOperation getCallOperation() {
        return callOperation;
    }

    public void setCallOperation(final CallOperation callOperation) {
        this.callOperation = callOperation;
    }

    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        final Action action = (Action) o;
        return Objects.equals(callOperation, action.callOperation);
    }

    @Override
    public int hashCode() {
        return Objects.hash(callOperation);
    }
}
