package com.ericsson.am.shared.vnfd.model;

import java.util.Objects;

public class DataTypeImpl extends DataType {

    private String dataTypeName;

    public String getDataTypeName() {
        return dataTypeName;
    }

    public void setDataTypeName(String dataTypeName) {
        this.dataTypeName = dataTypeName;
    }

    @Override
    public boolean equals(final Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        if (!super.equals(o)) {
            return false;
        }
        final DataTypeImpl dataType = (DataTypeImpl) o;
        return Objects.equals(dataTypeName, dataType.dataTypeName);
    }

    @Override
    public int hashCode() {
        return Objects.hash(super.hashCode(), dataTypeName);
    }
}
